package week1.day2;

public class Calculator {

	public int addthreeNumbers(int a, int b, int c) {
		return a + b + c;

	}

	public int subthreenumbers(int a, int b, int c) {
		return a - b - c;
	}

	public double multhreenumbers(double a, double b, double c) {
		return a * b * c;
	}

	public int dividethreenumbers(int a, int b, int c) {
		return a / b / c;
	}

}
